namespace app\api\service\netease\im;

use app\vendor\log\Monolog;

class NimserverTeamLeave extends Base
{

    /**
     * 主动退群
     *
     * tid   String  是   网易云通信服务器产生，群唯一标识，创建群时会返回
     * accid   String  是   退群的accid
     * @param  [type] $params [description]
     * @return
     * 返回示例
     * ["Content-Type": "application/json; charset=utf-8"
     *{
     *  "code":200 
     *}       
     * ]
     */
    public function request($params)
    {
        try {
            $response = $this->httpClient->post(
                $this->apiConf['nimserver.team.leave'],
                [
                    'form_params' => $params,
                    'headers'     => $this->headers,
                ]
            );
            $body = $response->getBody()->getContents();
            Monolog::info(':', [$body]);
            return json_decode($body, true);
        } catch (\Exception $e) {
            Monolog::error('主动退群失败,[NimserverTeamLeave]' . $e->getMessage(), [$params]);
        }
        return false;
    }

}
